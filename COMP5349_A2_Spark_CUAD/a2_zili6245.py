def caculateEnd(row):
  """
  Calculate end based on the answer and start
  """
  title = row[0][0]
  context = row[0][1]
  que = row[1][0]
  answer_dict = row[1][1].asDict()

  # caculate end location
  text = answer_dict['text']
  start = answer_dict['answer_start']
  end = start + len(text)
  return ((title, context), (que, (text, (start, end))))

